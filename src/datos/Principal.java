    private void btnElejirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElejirArchivoActionPerformed
        JFileChooser fc = new JFileChooser();
        int opc = fc.showOpenDialog(this);
        if (opc == JFileChooser.APPROVE_OPTION) {
            txtArchivo.setEnabled(true);
            txtArchivo.setText(fc.getSelectedFile().getPath());
        }
    }//GEN-LAST:event_btnElejirArchivoActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int opc = JOptionPane.showConfirmDialog(this, "Seleccion√≥ el archivo\n" + txtArchivo.getText() + "Desea continuar", "Elejir Archivo", JOptionPane.YES_NO_OPTION);
        if (opc == 0) {
            Banco.archivo = txtArchivo.getText();
        }else{
            btnElejirArchivoActionPerformed(evt);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

